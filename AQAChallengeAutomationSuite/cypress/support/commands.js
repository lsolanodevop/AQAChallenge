// Helper function for taking screenshots with a standardized naming convention
Cypress.Commands.add('captureScreenshot', (screenshotName) => {
    // Create a standardized screenshot name
    const name = screenshotName || `screenshot-${Date.now()}`;
    cy.screenshot(name);
  });